# Math in Python
import math

min = min(5, 10, 25)
print(min)

max = max(5, 10, 25)
print(max)

absolute = abs(-7.99)
print(absolute)

power = pow(4, 3)
print(power)

square_root = math.sqrt(100)
print(square_root)

ceiling = math.ceil(1.2)
print(ceiling)

floor = math.floor(1.2)
print(floor)

print(math.pi)